# 算术运算符
# +   -   *   /   //    %   **

# 比较运算符
# ==  !=  >   >=   <   <=

# 逻辑运算符
# and   or   not

# 赋值运算符
# = +=   -=   *=   /=   //=    %=   **=

# 我们可以使用这些运算符来构建表达式，例如：

# 算术表达式
