
# 在这一课中，我们将重点介绍运算符和表达式。运算符用于执行数学或逻辑运算，而表达式则是由运算符和操作数（即被操作的变量或常量）组成的组合，用于产生特定结果。
# Python支持多种类型的运算符，其中包括算术运算符、比较运算符、逻辑运算符和赋值运算符等。
